print(3 * (2 + 7))  # Affiche 27

# Afficher la différence entre 20 et le produit de 4 et 3 (N - multiplication)
print(20 - (4 * 3))  # Affiche 8

# Afficher la puissance de la somme de 2 et 3 exposant 4 (addition ** N)
print((2 + 3) ** 4)  # Affiche 625

# Afficher le quotient entier de la somme de 15 et 5 divisé par 4 (addition // N)
print((15 + 5) // 4)  # Affiche 5

# Afficher le reste de la division de la différence de 25 et 10 par 6 (soustraction % N)
print((25 - 10) % 6)  # Affiche 3

